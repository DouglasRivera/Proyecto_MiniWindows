    private void jRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRegistrarActionPerformed
        boolean Valido=true;
        if(txtNickname.getText() == null || "".equals(txtNickname.getText())){
            JOptionPane.showMessageDialog(this, "Ingresar un nombre de usuario");
            Valido=false;
        }
        if(txtNombre.getText() == null || "".equals(txtNombre.getText())){
            JOptionPane.showMessageDialog(this, "Ingresar un nombre");
            Valido=false;
        }
        if(txtApellido.getText() == null || "".equals(txtApellido.getText())){
            JOptionPane.showMessageDialog(this, "Ingresar un Apellido");
            Valido=false;
        }
        if(txtContra.getText() == null || "".equals(txtContra.getText())){
            JOptionPane.showMessageDialog(this, "Ingresar una contrase単a");
            Valido=false;
        }
        if("".equals(txtConfirm.getText()) || !txtConfirm.getText().equals(txtContra.getText())){
            JOptionPane.showMessageDialog(this, "Las contrase単as no coinciden");
            Valido=false;
        }
        if(Valido){
            //Pendiente de guardar en base de datos
            File CarpetaUser = new File(Constantes.RutaRaiz+File.separator+txtNickname.getText().trim());
            CarpetaUser.mkdir();
            CarpetaUser = new File(Constantes.RutaRaiz+File.separator+txtNickname.getText().trim()+File.separator+Constantes.MisDocumentos);
            CarpetaUser.mkdir();
            CarpetaUser = new File(Constantes.RutaRaiz+File.separator+txtNickname.getText().trim()+File.separator+Constantes.MisImagenes);
            CarpetaUser.mkdir();
            CarpetaUser = new File(Constantes.RutaRaiz+File.separator+txtNickname.getText().trim()+File.separator+Constantes.MiMusica);
            CarpetaUser.mkdir();
            CarpetaUser = new File(Constantes.RutaRaiz+File.separator+txtNickname.getText().trim()+File.separator+Constantes.MiCalendario);
            CarpetaUser.mkdir();
            CarpetaUser = new File(Constantes.RutaRaiz+File.separator+txtNickname.getText().trim()+File.separator+Constantes.MisMensajes);
            CarpetaUser.mkdir();
            CarpetaUser = new File(Constantes.RutaRaiz+File.separator+txtNickname.getText().trim()+File.separator+Constantes.MiEnvioCorreo);
            CarpetaUser.mkdir();
            
        }
    }//GEN-LAST:event_jRegistrarActionPerformed

